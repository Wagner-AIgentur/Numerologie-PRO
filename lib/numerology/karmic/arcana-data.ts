/**
 * Получить данные аркана по номеру.
 * Если номер > 22, вычитаем 22.
 * Если номер === 22, возвращаем аркан 22 (Шут).
 *
 * @param locale - 'de' für Deutsch, 'ru' für Russisch (default)
 */
export function getArcana(number: number, locale: 'de' | 'ru' = 'ru'): ArcanaData {
  if (number > 22) number -= 22;

  if (locale === 'de') {
    // Lazy import to avoid circular deps
    const { ARCANA_DE } = require('./arcana-data-de');
    return ARCANA_DE[number];
  }

  return ARCANA[number];
}
